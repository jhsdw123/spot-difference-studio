import puppeteer from 'puppeteer';
const browser = await puppeteer.launch({ headless: 'new' });
const page = await browser.newPage();
const errors = [];
page.on('pageerror', e => errors.push(e.message));
page.on('console', m => { if (m.type() === 'error') errors.push(m.text()); });
await page.setViewport({ width: 390, height: 844, isMobile: true, hasTouch: true });
await page.goto('https://spothuntstudio.com/spot-hunt/', { waitUntil: 'networkidle0', timeout: 60000 });
await page.waitForFunction(() => window.__sh && window.__sh.state.sequence.length > 0, { timeout: 25000 });
console.log('LIVE pool:', await page.$eval('#home-pool', e => e.textContent));
await page.tap('#btn-play');
await page.waitForFunction(() => !document.querySelector('#veil').classList.contains('on'), { timeout: 30000 });
const regions = await page.evaluate(() => window.__sh.state.puzzle.regions);
const box = await page.$eval('#panel-b', el => { const r = el.getBoundingClientRect(); return { x: r.x, y: r.y, w: r.width }; });
await page.touchscreen.tap(box.x + regions[0].x / 100 * box.w, box.y + regions[0].y / 100 * box.w);
await new Promise(r => setTimeout(r, 400));
console.log('LIVE first find:', await page.$eval('#found-count', e => e.textContent));
console.log('SW registered:', await page.evaluate(async () => !!(await navigator.serviceWorker.getRegistration())));
console.log('errors:', errors.length ? errors : 'none');
await browser.close();
